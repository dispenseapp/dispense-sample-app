export enum MediaQueryValue {
  screenXs = 480,
  screenSm = 768,
  screenMd = 992,
  screenLg = 1200,
}

export enum MediaQuery {
  screenXs = `${MediaQueryValue.screenXs}px`,
  screenSm = `${MediaQueryValue.screenSm}px`,
  screenMd = `${MediaQueryValue.screenMd}px`,
  screenLg = `${MediaQueryValue.screenLg}px`,
}
